/*
** ADICIONAR UMA QUEBRA DE LINHA A UMA STRING
*/

#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
/*	escrever cada caractere de uma string e ao final acrescentar uma quebra de linha */
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
