/*
** VERIFICAR SE NOSSA VARIAVEL E UMA LETRA OU UM NUMERO
*/

#include "libft.h"

int	ft_isalnum(int c)
{
/*	'0' a '9' ou de 'Aa' a 'Zz' */
	if ((c >= 48 && c <= 57) || (c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}
