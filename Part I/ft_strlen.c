/*
** CONTAR A QUANTIDADE DE CARACTERES DE UMA STRING
*/

#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
/*	enquanto minha string 's' nao chegar ao fim iremos incrementar nosso conta\
dor 'i' */
	while (s[i] != 0)
		i++;
/*	nosso contador 'i' possui o tamanho de nossa string */
	return (i);
}
